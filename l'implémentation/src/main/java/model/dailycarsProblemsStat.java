package model;

public class dailycarsProblemsStat {
	private int dayIndex;
	private int dailyPending;
	private int dailyActive;
	private int dailyCompleted;
	
	public int getDayIndex() {
		return dayIndex;
	}
	public void setDayIndex(int dayIndex) {
		this.dayIndex = dayIndex;
	}
	public int getDailyPending() {
		return dailyPending;
	}
	public void setDailyPending(int dailyPending) {
		this.dailyPending = dailyPending;
	}
	public int getDailyActive() {
		return dailyActive;
	}
	public void setDailyActive(int dailyActive) {
		this.dailyActive = dailyActive;
	}
	public int getDailyCompleted() {
		return dailyCompleted;
	}
	public void setDailyCompleted(int dailyCompleted) {
		this.dailyCompleted = dailyCompleted;
	}
}
